    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String idbn,titulo,autor,anio,costo,paginas,edicion;
        String au="RegistroPU.bin";
        Scanner leer=null;
        File fw=new File(au);
        try {
            leer=new Scanner(fw);
            while(leer.hasNextLine()){
                idbn=leer.nextLine();
                titulo=leer.nextLine();
                autor=leer.nextLine();
                anio=leer.nextLine();
                costo=leer.nextLine();
                paginas=leer.nextLine();
                edicion=leer.nextLine();
                model.addRow(new Object[]{idbn,titulo,autor,anio,costo,paginas,edicion});
            }
        } catch (Exception x) {
            JOptionPane.showMessageDialog(null, "Error");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            FileWriter salvar=new FileWriter("RegistroPU.bin");
            for(int i=0;i<this.jTable1.getRowCount();i++){
                salvar.write(model.getValueAt(i, 0).toString()+"\n");
                salvar.write(model.getValueAt(i, 1).toString()+"\n");
                salvar.write(model.getValueAt(i, 2).toString()+"\n");
                salvar.write(model.getValueAt(i, 3).toString()+"\n");
                salvar.write(model.getValueAt(i, 4).toString()+"\n");
                salvar.write(model.getValueAt(i, 5).toString()+"\n");
                salvar.write(model.getValueAt(i, 6).toString()+"\n");
            }
            salvar.close();
            JOptionPane.showMessageDialog(null, "Datos guardados");
        } catch (Exception x) {
            JOptionPane.showMessageDialog(null, x);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

